'''
    二维向量类
'''

class Vector2:
    '''
        二维向量基础类： 坐标 与 方向
    '''
    def __init__(self,x,y):
        self.x = x 
        self.y = y 
    
    @staticmethod
    def right():  #右
        return Vector2(0,1)
    @staticmethod
    def up():  #上
        return Vector2(-1,0)
    @staticmethod
    def left():  #左
        return Vector2(0,-1)
    @staticmethod
    def down():  #下
        return Vector2(1,0)
    
class DoubleListHelper:
    '''
        二维向量助手类：
    '''
    @staticmethod
    def get_element(list_target, v_pos, v_dir, count):
        '''
        :param list_target:  二维数据表
        :param v_pos: 起始位置,  相对二维数据表 的下标位置 例：Vector2(5,8) //第6行，第9列
        :param v_dir: 方向 2维的
        :param count: 移动距离
        :return:  返回列表类型  二维坐标
        '''
        retulist = []
        for  i in range(count):
            v_pos.x += v_dir.x
            v_pos.y += v_dir.y
            retulist.append(list_target[v_pos.x][v_pos.y])
        return retulist

